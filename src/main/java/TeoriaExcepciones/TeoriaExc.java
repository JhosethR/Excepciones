public class TeoriaExc {
  //   En Java, una excepción es un objeto que representa un problema que ha ocurrido durante la ejecución de un programa. 
 //   Las excepciones se utilizan para manejar situaciones inesperadas o errores que pueden surgir mientras se ejecuta un programa.

//Existen diferentes tipos de excepciones en Java, que se dividen en dos categorías principales: 
//excepciones comprobadas (checked exceptions) y excepciones no comprobadas (unchecked exceptions).

//Excepciones comprobadas (Checked Exceptions): Estas son aquellas que el compilador de Java obliga a manejar explícitamente mediante un 
//bloque try-catch o declarando que el método puede lanzar esa excepción usando la palabra clave throws. 
//Ejemplos comunes de excepciones comprobadas son IOException y SQLException.
    
//Excepciones no comprobadas (Unchecked Exceptions): Estas son subclases de RuntimeException y sus subclases. 
//No es necesario manejarlas explícitamente en el código. Ejemplos comunes incluyen NullPointerException, ArrayIndexOutOfBoundsException y ArithmeticException.



//Las excepciones se utilizan para:

//Detectar y manejar errores: Permiten al programa detectar errores y manejarlos de manera adecuada en lugar de simplemente detenerse abruptamente.
//Mejorar la legibilidad del código: Al manejar excepciones, puedes separar el código de manejo de errores del código principal, }
//lo que mejora la legibilidad y mantenibilidad del código.
//Facilitar la depuración: Las excepciones proporcionan información útil sobre qué salió mal y dónde ocurrió el error, lo que facilita la depuración del código.
//Proporcionar retroalimentación al usuario: Puedes usar excepciones para informar al usuario sobre problemas que ocurrieron durante la ejecución 
//del programa y cómo manejarlos.  
}
